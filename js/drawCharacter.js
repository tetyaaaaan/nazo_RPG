// 1つのドットのサイズを定義する
const dotSize = 5;

// カラーパレットを定義する
const colorPalette = ["#ffffff", "#aaaaaa", "#555555", "#000000"];

// ドット絵を描画する関数
function drawDotArt() {
  for (let row = 0; row < dotPattern.length; row++) {
    for (let col = 0; col < dotPattern[row].length; col++) {
      const dot = dotPattern[row][col];
      const x = col * dotSize;
      const y = row * dotSize;

      ctx.fillStyle = colorPalette[dot];
      ctx.fillRect(x, y, dotSize, dotSize);
    }
  }
}

// ドット絵を描画する
drawDotArt();